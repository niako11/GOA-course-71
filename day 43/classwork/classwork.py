# 1) ფუნქცია, რომელიც რიცხვს ორმაგს აბრუნებს
def double_value(num):
    return num * 2

# 2) ფუნქცია, რომელიც იღებს 3 რიცხვს და აბრუნებს (პირველი + მეორე) / მესამე-ს
def sum_divide(a, b, c):
    if c == 0:
        return "გაყოფა 0-ზე შეუძლებელია"
    return (a + b) / c

# 3) ფუნქცია, რომელიც სიის ყველა ელემენტს დაბეჭდავს
def print_list_elements(lst):
    for item in lst:
        print(item)

# 4) განსხვავება print და return შორის:
# print - ფუნქცია, რომელიც უბრალოდ ტექსტს ან მნიშვნელობას ბეჭდავს ეკრანზე, 
#          მაგრამ არ აბრუნებს არაფერს პროგრამისთვის.
# return - გამოაგზავნის მნიშვნელობას ფუნქციიდან, რომლითაც შეიძლება შემდგომი მუშაობა.

# მაგალითად: 
# print( double_value(3) ) ბეჭდავს 6-ს, მაგრამ თავად double_value ფუნქცია არ ინახავს ამ მნიშვნელობას.
# თუ double_value ფუნქციაში არ იქნას გამოყენებული return, შედეგი გადახდება.

# --- მაგალითი გამოყენების ---

res1 = double_value(5)       # res1 = 10
res2 = sum_divide(4, 6, 2)  # res2 = 5.0

print_list_elements(["ანა", "ბერა", "გიორგი"])

print("double_value(5) =", res1)
print("sum_divide(4,6,2) =", res2)
