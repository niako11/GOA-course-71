

# დასაზუსტებელი (Deterministic): ყოველთვის ერთნაირ შედეგს იძლევა.

# არადასაზუსტებელი (Non-deterministic): ერთნაირ შეყვანაზე განსხვავებულ შედეგს იძლევა.

