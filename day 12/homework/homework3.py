# == ტოლობა
print(5 == 5)      # True
print(10 == 20)    # False
print("hi" == "hi")# True

# != არათოლობა
print(5 != 3)      # True
print(10 != 10)    # False
print("a" != "b")  # True

# > მეტია
print(7 > 3)       # True
print(2 > 8)       # False
print(5 > 5)       # False

# < ნაკლებია
print(3 < 7)       # True
print(9 < 4)       # False
print(6 < 6)       # False

# >= მეტია ან ტოლი
print(10 >= 5)     # True
print(7 >= 7)      # True
print(4 >= 9)      # False

# <= ნაკლებია ან ტოლი
print(2 <= 5)      # True
print(8 <= 8)      # True
print(10 <= 3)     # False
